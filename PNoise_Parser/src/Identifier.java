public class Identifier
{
	public String var, value;
	public Identifier()
	{

	}

	public void setIdentifierVar(String var)
	{
		this.var = var;
	}

	public String getIdentifierVar()
	{
		return var;
	}

	public void setIdentifierValue(String value)
	{
		this.value = value;
	}

	public String getIdentifierValue()
	{
		return value;
	}
}